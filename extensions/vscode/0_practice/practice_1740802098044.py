# 기본 데이터 타입 예제
name = "홍길동"        # 문자열(str)
age = 25              # 정수(int)
height = 175.5        # 실수(float)
is_student = True     # 불리언(bool)

# 데이터 타입 확인
print(f"name의 타입: {type(name)}")
print(f"age의 타입: {type(age)}")
print(f"height의 타입: {type(height)}")
print(f"is_student의 타입: {type(is_student)}")